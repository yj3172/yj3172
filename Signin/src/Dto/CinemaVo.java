package Dto;

public class CinemaVo {
	String cine;
	String doo;
	String si;
	String sponsor;
	String Cineinfo;
	String num;
 
	public String getNum() {
		return num;
	}
	public void setNum(String num) {
		this.num = num;
	}
	public String getCine() {
		return cine;
	}
	public void setCine(String cine) {
		this.cine = cine;
	}
	public String getDoo() {
		return doo;
	}
	public void setDoo(String doo) {
		this.doo = doo;
	}
	public String getSi() {
		return si;
	}
	public void setSi(String si) {
		this.si = si;
	}
	public String getSponsor() {
		return sponsor;
	}
	public void setSponsor(String sponsor) {
		this.sponsor = sponsor;
	}
	public String getCineinfo() {
		return Cineinfo;
	}
	public void setCineinfo(String cineinfo) {
		Cineinfo = cineinfo;
	}
	
	
}
